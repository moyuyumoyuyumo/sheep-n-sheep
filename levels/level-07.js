//
// 牌数验算：
//   底 z=0: 16 × 4 = 64
//   z=1 (offsetX=3, offsetY=0.5): 10 × 3 = 30
//   z=2 (offsetX=4, offsetY=1.0): 8  × 3 = 24
//   z=3 (offsetX=5, offsetY=1.5): 6  × 3 = 18
//   z=4 (offsetX=6, offsetY=2.0): 4  × 3 = 12
//   z=5 (offsetX=7, offsetY=2.5): 2  × 3 = 6
//   z=6 (offsetX=7, offsetY=3.0): 2  × 1 = 2
//   ─────────────────────────────────────
//   合计 64+30+24+18+12+6+2 = 156 = 52 套 ✓（3 的倍数）
//   13 种符号 × 4 套 × 3 = 156 ✓（每种正好 12 张）

